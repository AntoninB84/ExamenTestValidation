package com.example.exercice2

class Calcul {

    fun add(a: Int, b: Int): Int {
        return a + b;
    }

    fun sub(a: Int, b: Int): Int {
        return (a - b)
    }

    fun div(a: Float, b: Float): Float {
        return a/b
    }

    fun multiple(a: Int, b: Int): Int {
        return a*b
    }
}